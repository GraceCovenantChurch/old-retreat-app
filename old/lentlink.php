	<p><strong><em>OBSERVING THE LENTEN SEASON</em></strong><br>&nbsp;</p>

	<p>Lent has been observed as the period from Ash Wednesday to Easter Sunday. As a church tradition, it is a time spent in anticipation of Easter.  As our church participates in Lent, let us not consider it a time of only fasting from the world, but rather a feast on the presence of Christ.</p>
	<p>There are four things that should occur during this time period.  They are the 4 R's:</p>
	<p>1) It is a time of <em>REFLECTION</em><blockquote>Lent is a time to refocus and reflect on the suffering, death and resurrection of our Savior, Jesus Christ.  It is a time of readiness for Christians as we prepare to observe and celebrate the Passion Week.</blockquote></p>
	<p>2) It is a time of <em>REPENTANCE</em><blockquote>In the Lenten season, self-examination is crucial.  As we reflect on the cross, it should expose our pride, our self-sufficiency and our disobedience.  Pray that God would show His kindness that leads us to repentance and to turn back to Him.</blockquote></p>
	<p>3) It is a time of <em>RESOLUTION</em><blockquote>One key component of Lent is to practice the spiritual discipline of fasting.  It does not necessarily have to be food, but anything which clutters our lives or impedes us from being in relationship with God...that is why it is different for different people.  For some it might be TV, for others it might be shopping, for others coffee, etc...</blockquote></p>
	<p>4) It is a time of <em>RENEWAL</em><blockquote>Lent is known as a time of spiritual renewal and growth.  As we focus on Jesus, who died so that we may live, it will renew our passion and our love.</blockquote></p>

	<p><strong>What is the history of it?</strong></p>
        <p>For Christians, Lent is a process of prayer and spiritual renewal.  The Lenten season emphasizes one’s need to cultivate a deeper walk with Christ through spiritual exercises and disciplines.</p>
	<p>Originally, in the first century, Lent was observed as a 40-hour period, in keeping with the forty hours Jesus’ body was in the tomb.  The forty days of Lent (Sundays are not included, for they are the Lord’s Day, which includes a celebration of the resurrection) call us to be dedicated to discipline and personal examination.</p>

	<p><strong>Why should we observe it?</strong></p>
        <p>There are many evangelical Christians who responsd to the observance of Lent by saying the following things:</p>
	<blockquote><ul>
		<li>"Why should we observe Lent?  Shouldn't we have the death and resurrection of Christ always in our hearts?"</li>
		<li>"Why be so legalistic?  We are now free in Christ, we don't need to try to live a life of self-denial."</li>
		<li>"Isn't Lent more of a tradition?  Shouldn't we do things more out of conviction?"</li>
	</ul></blockquote>

	<p>In response to those questions:</p>
	<blockquote><ul>
                <li>Setting aside some special time to remember something is a good thing to do.  We are forgetful people.  Even though the death and resurrection of Christ should always be in our hearts, we forget.  Lent is an opportunity to refocus on the love of Jesus.</li>
                <li>Legalism is taking something that is “God-centered” and then making it “man-centered.”  Legalism is binding and it puts us in bondage, while doing things (i.e. spiritual disciplines) that are God-centered is liberating and releases us to love God more.  Living a life of self-denial is not only a command of Jesus but those who practice it are truly “free in Christ” to say, “Yes” to God and “No” to the things of this world.</li>
                <li>Tradition can be a good thing.  Tradition gets to be a burden or a strain when we lose the purpose of it.  Conviction grows as we begin to pray and understand the purpose of why we do things.</li>
        </ul></blockquote>

	<p>In general, observing Lent should benefit your spiritual life.  It will increase your passion for God and it will bring a greater heart of thankfulness for the cross.</p>

	<p><strong>How should we observe it?</strong></p>
        <p>Once again, this is where our freedom in Christ comes in.  Everyone is different; therefore we will observe Lent in various ways.  There is no one way to do things.  But there are some basic principles to remember:</p>
	<p>1) <b>Try to intensify your commitment to God within this time period.</b>  Commit to read the Word and spend time in prayer.</p>
	<p>2) <b>Try to "fast" from something.</b>  Keep in mind that is should cost us something.  What I mean by "cost" is this: If you are not a breakfast eater and then you decide to give up breakfast for Lent, then it is not costing you anything.  Try to fast from something you know that at times it will be difficult.  Here are some examples:</p>
	<blockquote><ul>
		<li>Fast from TV or video games.</li>
		<li>Fast from chocolates or coffee.</li>
		<li>Fast from going out to restaurants.</li>
	</ul></blockquote>
	<p>3) <b>Try to prepare for Good Friday and Resurrection Sunday.</b><br>&nbsp;</p>

	<p>Also there are some things to remember in the midst of observing Lent:</p>
	<p>1) Remember not to judge.  Don't look down on people who are not actively observing Lent.  Remember it does not make you more spiritual.  If we are not careful it can lead to spiritual pride.</p>
	<p>2) Remember not to get discouraged.  If you fail, pick yourself up and try again.  Those a righteous man falls seven times, he rises again (Pr 24:16).</p>
	<p>3) Remember to focus on Jesus.  It is all about what Christ did for us on the cross.  May we never forget!</p>
	
