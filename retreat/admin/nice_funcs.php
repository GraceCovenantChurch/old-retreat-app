<?php

function makeGrayHeader($text) {
  echo "<div class='grayHeader'><h1>$text</h1></div>\n";
}

function makeYellowHeader($text, $options=array()) {
  $widthStyle = ($options["width"]) ? "style='width: $options[width]'" : "";
  echo "<div class='yellowHeader' $widthStyle><h1>$text</h1></div>\n";
}

function makeBlueHeader($text, $options=array()) {
  $widthStyle = ($options["width"]) ? "style='width: $options[width]'" : "";
  echo "<div class='blueHeader' $widthStyle><h1>$text</h1></div>\n";
}

?>