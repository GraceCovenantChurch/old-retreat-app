	<h4>Frequently Asked Questions</h4><br>
    <p><b>What is a typical Sunday Service like?</b><br>
We welcome you at the front door. And then begin with contemporary worship lead by our praise band.  Next are announcements and offering.  Then a dynamic message from one of our pastors followed by a time of reflection and prayer.   Then bustling interaction in the lobby with snacks included.</p>

	<p><b>What is the dress code?</b><br>
Dress casually and comfortably, as if you were going over to your friend's house.  We know that our Lord does not value our outwards appearances, but the desires of our hearts.  That being said, please be considerate of others around you and refrain from wearing clothes that may appear offensive.</p>

	<p><b>What is the music like?</b><br>
We have contemporary Christian praise and worship.  Some artists would include Hillsong United, Desperation Band, Chris Tomlin, and Matt Redman.</p>

	<p><b>Do only college students come to this church?</b><br>
No! We have over 100 young adults comprised of young married couples, grad students, and working professionals.<br>
We are about 2/3 college students and 1/3 young adults... from Upenn, Drexel, Jefferson, University of the Sciences and various parts of the city.</p>

	<p><b>What is expected of newcomers?</b><br>
No large audience public introductions are needed.  We do invite you to a newcomers information time after the service and to fill out a connection card.</p>

	<p><b>Where do I park?</b><br>
