<p>
Praise Night
<p>
Every year, family groups come together on Praise Night to worship the Lord through skits, songs, or other creative performances.  It is an opportunity for both newcomers and church members to learn about God in a different way.  The theme for this year’s Praise Night is “Two Ways to Live,” in reference to Matthias Media’s gospel summary.
<p>
Praise Night will be on Saturday, February 24, at 7:00 p.m. in Meyerson Hall.
<p>
<b>Prayer Requests:</b>
<ul>
<li><b>Groups:</b> Pray that the family groups bond through their practices and that their performances will truly be an act of worship.  Pray that the groups collaborate in a productive and loving way and that each member grows through this experience.</li>
<li><b>P</b>ray that God would use this night to speak to those who may not otherwise get exposure to the gospel message.</li>
<li><b>P</b>ray that the gospel be portrayed clearly and accurately and that the skits cover all the core values of the gospel.</li>
<li><b>Issues:</b> Many skits heavily rely on multimedia, so technical difficulties can disrupt the flow of the night and compromise a skit’s message.  Pray that this aspect of the night runs smoothly.</li>
</ul>
