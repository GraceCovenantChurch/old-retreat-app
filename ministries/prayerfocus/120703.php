                            which means &#8220;Enough is enough&#8221; and stormed 
                            the Georgian parliament. The president agreed to resign 
                            the last week of November, making Georgia&#8217;s 
                            biggest power transition in a decade a bloodless one, 
                            earning it the name &#8220;the velvet revolution&#8221;. 
                            Nino Burjanadze, the speaker of the last parliament 
                            and one of the opposition leaders, was then sworn 
                            in as the temporary president.</p>
