                            both of which are fighting for their independence.</p>
                          <p align="left"><strong>Enough 
                            is Enough</strong><br>
                            To many Georgians Mr. Shevardnadze - once renowned 
                            for his role as Soviet foreign minister in ending 
                            the Cold War - is a failed leader who has been unable 
                            to deliver on issues such as rampant poverty and corruption, 
                            energy crises and resolution of conflicts such as 
                            that of the breakaway region of Abkhazia. On November 
                            2nd, the country held what the president promised 
                            to be democratic elections. </p>
                          <p align="left">It 
                            was quickly evident that Mr. Shevardnadze also failed 
                            to follow through with this promise and Georgians 
                            turned up by the thousands to protest rigged results 
                            and broken promises. The people adopted the slogan 
