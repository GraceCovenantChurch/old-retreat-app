<span style="font-weight: bold;">Easter</span><br /><br />“I want to know Christ and the power of His resurrection and the fellowship of sharing in His sufferings, becoming like Him in His death, and so, somehow, to attain to the resurrection from the dead.” - Philippians 3:10-11
<p>
Jesus’ resurrection from the dead, three days after His crucifixion, is the cornerstone of our faith.  We know that because Jesus is alive, one day we will live forever.  Let’s pray for a better understanding of the weight of that truth, and the implications it has on our lives today.
</p>
<p><b>Prayer Requests:</b></p>
<ul>
<li><b>Thankfullness: </b>Thank and praise the Lord for His sacrifice - we are saved by His grace alone.  Pray that we can carry this thankfulness with us and that it would be manifested in our actions.</li>
<li><b>Celebration: </b>Celebrate in Christ’s victory over death and sin, and the relationship with God that has been made possible through it.  Pray that this joy does not fade as Easter passes.</li>
<li><span style="font-weight: bold;">Life: </span>Pray that Jesus’ resurrection - and the power found in it - can impact our lives in a profound way.</li></ul>
