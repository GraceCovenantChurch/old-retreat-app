<p>
Wycliffe’s vision is to see that God’s Word is accessible to all people in a language that speaks to their hearts. Their current mission is to see a Bible translation program in progress in every language still needing one by 2025. The provision of God’s Word in the mother tongue of each people is fundamental to world evangelization. Wycliffe believes that the most effective means of communication is the mother tongue. For a church to be truly indigenous, it must have the Bible in its mother tongue. Yet there are some 2,500 language groups that still need God’s Word, representing more than 270 million people. Since its founding in 1942, Wycliffe has helped to complete the New Testament for 623 languages and is currently involved with 1,294 languages.
<p>
