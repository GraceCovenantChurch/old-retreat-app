                      <p align="left"><strong>Senior Class - April 22, 2007</strong></p>
<p>
As the academic year comes to a close, the seniors find themselves on the cusp of young adulthood, about to take the next big step in their lives.  For most, it is a bittersweet time that holds excitement and new opportunities as well as nostalgia and uncertainties.
<p>
Please pray that they would continue to love God even after they are removed from the familiar community of GCC and that they would not forget the training they underwent while in college.
<p>
<b>Prayer Requests:</b>
<ul>
<li><b>Good Finish:</b> Pray that the seniors finish their college years faithfully.</li>
<li><b>Graduation:</b> Pray that God will use graduation to bring families and friends together safely to celebrate.</li>
<li><b>Future:</b> Many seniors are still unsure of what they will be doing after graduation.  Pray that those seniors would be able to surrender their futures to God and that God would open the right doors for them.  Pray even for those who do have jobs, as many of them are unsure whether it is really what they want to or should be doing.</li>
<li><b>Unity:</b> Pray that the seniors would continue to have unity even after college, and that the community would be encouraging and edifying.</li>
</ul>
