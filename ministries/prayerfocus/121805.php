Last year, about 300,000 were killed and 2.5 million people fled their homes in Darfur after militias orchestrated a campaign of total terror, including indiscriminate shootings, rape, and village razing.  Former Secretary of State called the atrocities “genocide” and UN aid agencies have labeled it one of the world’s worst humanitarian crises.  
