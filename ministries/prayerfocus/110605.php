                      <p align="left"><strong>India: Christian Dalits - November 6, 2005</strong></p>
<p>
<p>
About 18% of India’s massive population (over 1 billion) are classified as members of Dalit, a traditionally exploited and subjugated caste.  Although the caste system is illegal and unconstitutional in India, these citizens continue to lack many civil rights.  Dalits have been an exploding Christian population, which overall accounts for only 2.4% of the entire nation.  
<p>
However, the government currently discriminates against specifically the Dalits who convert to Christianity by revoking the affirmative action benefits instated to counteract the deeply-rooted caste mentality.  Dalits of Hindu, Sikh or Buddhist faith remain qualified.  A pending decision from the Supreme Court of India is forthcoming regarding the constitutionality of this discriminatory system.
<p>
<b>Please pray for:</b>
<ul>
 <li><b>Justice in the hearts of the Indian Supreme Court.</b>  Pray that the the Supreme Court would decide justly against the discrimination against the Christian Dalits. Many Dalits hesitate to convert solely because of the government discrimination that would leave them with basic needs.</li>
 <li><b>A spiritual awakening.</b>  A breakthrough in India would change the spiritual outlook of the whole continent.  Pray that our Lord Jesus would be glorified as He chooses the weak to shame the strong (1 Cor 1:26-29).</li>
</ul>
