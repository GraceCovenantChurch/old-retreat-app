41-year-old Abdul Rahman is being tried for the crime of being a professed convert from Islam to Christianity and may receive torture and public execution.  Although the Afghan constitution, based on Islamic Sharia law, makes assurances for religious freedom yet states that those who reject Islam can receive the death penalty.  The United States objects, along with other nations, stating that religious freedom is a key to instilling democracy.
