                          <p align="left">Nestling 
                            off the southern tip of India, the tropical island 
                            of Sri Lanka has beguiled travelers for centuries 
                            with its palm-fringed beaches, diverse landscapes 
                            and historical monuments. But for nearly two decades, 
                            the island was scarred by a bitter civil war arising 
                            out of ethnic tensions. The island has a long-established 
                            Tamil minority in the north and east. But the majority 
                            Buddhist Sinhalese community resented what they saw 
                            as favoritism towards the mainly-Hindu Tamils under 
                            British administration. </p>
                          <p align="left">The 
                            growth of a more assertive Sinhala nationalism after 
                            independence fanned the flames of ethnic division 
                            until civil war erupted in the 1980s between Tamils 
                            pressing for self-rule and the government. Most of 
                            the fighting took place in the north, but the conflict 
                            also penetrated the heart of Sri Lankan society with 
                            Tamil Tiger rebels carrying out devastating suicide 
                            bombings in Colombo in the 1990s. In the past 20 years, 
                            64,000 people have died due to the violence.</p>
                          <p align="left">The 
                            government and rebels reached a ceasefire and a political 
                            agreement in late 2002, which raised hopes for a lasting 
                            settlement. As peace talks progressed, the Tigers 
                            dropped their demand for independence and say they 
                            will now settle for regional autonomy - a major concession. 
                            The government also gave ground - this was the first 
                            time it had agreed to share power with the Tamil Tigers. 
                            </p>
                          <p align="left">Just 
                            this week, however, President Kumaratunga dismissed 
                            three ministers from her government after frustration 
                            at the peace deal being brokered by her political 
                            rival Prime Minister Ranil Wickramasinghe. The rivalry 
                            between these two has been shaping Sri Lanka&#8217;s 
                            politics for well over a decade and all hopes for 
                            future peace will depend heavily on their ability 
                            to cooperate with each other.</p>
