                      <p align="left"><strong>Kenya: A Gateway In Africa - April 02, 2006</strong></p>
<p>
Kenya, though an advanced hub for commerce in Africa, has recently been slowed by the AIDS pandemic, corruption and violent unrest.  The major religion in Kenya is Christianity and some schools of missions strategy have viewed Kenya as a doorway into other areas of Africa.  But as the nation continues to suffer, the healing of the Kenyan people and the ministry to the continent are strained.  Incidents stemming from internal strife like the Turbi massacre last July damage national stability and morale.  Most recently, Kenya has been in the news because of its prominence in the drug trafficking industry.
<p>
Kenya’s population of 32.8 million still has 35 unreached groups, although the major religion is Christianity. Dr. Steve Lee, a missionary with Africa Inland Mission and supported by GCC, is engaged with medical missions and church planting.  He and his family are based in Kapsowar in western Kenya.  For more information, click on www.theleefamily.net.
