                      <P align="left"><font face="Verdana, Arial, Helvetica, sans-serif" 
            size=2><strong>21-day Preparation for Easter - March 23, 2003</strong></P>
                        <p align="left"><strong>WHAT 
                          IS IT?</strong></p>
                        <p align="left">As 
                          a church, we would like to prepare ourselves for the 
                          remainder of the Easter season by &#8220;fasting&#8221; 
                          for the twenty-one days leading up to Good Friday.</p>
                        <p align="left">Five 
                          things should occur during this time period. They are 
                          the 5 R&#8217;s:</p>
                        <p align="left">1) 
                          It is a time of REFLECTION<br>
                          &#8226; A time to refocus and reflect on the suffering, 
                          death and resurrection of our Savior, Jesus Christ</p>
                        <p align="left">2) 
                          It is a time of REPENTANCE<br>
                          &#8226; Self-examination is crucial. As we reflect on 
                          the Cross, it should expose our pride, our self-sufficiency 
                          and our disobedience.</p>
                        <p align="left">3) 
                          It is a time of READINESS<br>
                          &#8226; A time of readiness for all members as we prepare 
                          to observe and celebrate the Passion Week.</p>
                        <p align="left">4) 
                          It is a time of RESOLUTION<br>
                          &#8226; A time to practice the spiritual discipline 
                          of fasting. It does not necessarily have to be food, 
                          but anything which clutters our lives or impedes us 
                          from being more focused on God.</p>
                        <p align="left">5) 
                          It is a time of RENEWAL<br>
                          &#8226; A time of spiritual renewal and growth. It is 
                          a time for people to experience and reflect on the sufferings 
                          of Jesus, in light of personal sin and unrighteousness. 
                          As we focus on Jesus, who died so that we may live, 
                          it will renew our passion and our first love for Him.</p>
                        <p align="left"><strong> 
                          WHY SHOULD WE OBSERVE IT?</strong></p>
                        <p align="left">There 
                          are many evangelical Christians who responds to the 
                          observance of Lent by saying the following things:<br>
                          </p>
                        <div align="left"> 
                          <ul>
                            <li>  
                              &#8220;Why should we observe lent? Shouldn&#8217;t 
                              we have the death and resurrection of Christ always 
                              in our hearts?&#8221;<br>
                              <br>
                              </li>
                            <li> &#8220;Why 
                              be so legalistic? We are now free in Christ, we 
                              don&#8217;t need to try to live a life of self-denial.&quot;<br>
                              <br>
                              </li>
                            <li>  
                              &#8220;Isn&#8217;t Lent more of a tradition? Shouldn&#8217;t 
                              we do things more out of conviction.&#8221;</li>
                          </ul>
                        </div>
                        <p align="left">In 
                          response to those questions:</p>
                        <div align="left"> 
                          <ul>
                            <li>  
                              Setting aside some special time to remember something 
                              or even to experience something is a good thing 
                              to do. We are forgetful people. Even though the 
                              death and resurrection of Christ should always be 
                              in our hearts, we forget to live this life. If we 
                              use this argument, then we should not have retreats, 
                              revival meetings or special church events. But this 
                              would be unreasonable because we need retreats. 
                              A retreat is an opportunity to set aside some special 
                              time to get away and renewal our hearts with God.<br>
                              <br>
                              </li>
                            <li> Legalism 
                              is taking something &#8220;God-centered&#8221; and 
                              then making it &#8220;man-centered.&#8221; Legalism 
                              is binding and it puts us in bondage, while doing 
                              things (i.e. spiritual disciplines) that are God-centered 
                              is liberating and releases us to love God more. 
                              Living a life of self-denial is not only a command 
                              of Jesus (Mk 8:34-35, Lk 14:25ff), but those who 
                              practice it are truly &#8220;free in Christ.&#8221; 
                              Free to say, &#8220;Yes&#8221; to God and &#8220;No&#8221; 
                              to the flesh and the things of this world.<br>
                              <br>
                            </li>
                            <li> Sometimes 
                              tradition is good. For example, some of us might 
                              come from backgrounds where it is a tradition to 
                              go on a family vacation once a year. This is a good 
                              thing because it re-emphasizes the value of the 
                              family, of one another and etc. Tradition gets to 
                              be a burden or a strain when we lose the purpose 
                              of it. Once we lose the purpose behind things, we 
                              just go through the motions, which always lead to 
                              half-heartedness. Conviction grows as we begin to 
                              pray and understand the purpose of why we do things.</li>
                          </ul>
                        </div>
                        <p align="left"><br>
                          <strong>HOW SHOULD WE OBSERVE IT?</strong></p>
                        <p align="left">The 
                          pastoral staff would like to invite the church (completely 
                          optional) to join as in a &#8220;Daniel fast&#8221;. 
                          Each of the three weeks leading up to Good Friday, we 
                          will be limiting the kinds of food that we consume. 
                          Our physical goal is to taper down for Passion Week 
                          (the week of Easter). Please do not &#8220;pig out&#8221; 
                          on the allowed/suggested foods. Our spiritual goal is 
                          that by limiting oneself and deepening our relationship 
                          with God, we will be reminded of His sacrifice and His 
                          presence in our lives. Below is the schedule for our 
                          fast:</p>
                        <p align="left">March 
                          27-28 (midnight to midnight): we will only drink water. 
                          At midnight on Friday, we will break fast together at 
                          Ardmore church, followed by a prayer vigil from 12:30am-6:00am. 
                          You may bring a sleeping bag.</p>
                        <p align="left">March 
                          29 &#8211; April 5: No meats, no coffee, and no sweets.</p>
                        <p align="left">April 
                          6-12: vegetable and fruit fast. Potatoes are included</p>
                        <p align="left">April 
                          13-17: juice fast</p>
                        <p align="left">April 
                          18: only water. The fast will be over following Good 
                          Friday service.</p>
                        <p align="left">NOTE: 
                          Again, this fast is optional. If you would like to take 
                          part in the fast for small periods of time, that would 
                          be welcomed. If you fail while fasting, don&#8217;t 
                          be discouraged. You can restart the fast at any time.</p>
                        <p align="left">Or, 
                          you may do your own kind of fast. The &#8220;spirit 
                          of fasting&#8221; is what matters the most. It is just 
                          between you and God. Below are some suggestions/examples:</p>
                        <p align="left">1) 
                          Try to &#8220;fast&#8221; from something. Keep in mind 
                          that it should cost you something. What I mean by &#8220;cost&#8221; 
                          is this: If you are not a breakfast eater and then you 
                          decide to give up breakfast, then it is not costing 
                          you anything. Try to fast from something you know that 
                          at times it will be difficult. Here are some examples:</p>
                        <div align="left"> 
                          <ul>
                            <li> 
                              Fast from TV or video games</li>
                            <li> 
                              Fast from chocolates or coffee</li>
                            <li> 
                              Fast from going out to restaurants</li>
                            <li> 
                              Fast from studying on Sundays (b/c you are diligent 
                              in getting everything done from M-Sat)</li>
                          </ul>
                        </div>
                        <p align="left">2) 
                          Try to spend time deepening your walk and intimacy with 
                          God. Make a vicious commitment to read the Word and 
                          spend extra time in prayer. We recommend following the 
                          Bible Reading Plan as a supplement to your devotional 
                          life.</p>
                        <p align="left">3) 
                          Try to prepare your heart for Good Friday and Resurrection 
                          Sunday 
                          </p>
