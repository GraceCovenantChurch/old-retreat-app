<p>
On Monday of last week, a major earthquake caused panic in six countries in Africa.  Its epicenter was in the Democratic Republic of Congo (DRC).  The death count is currently uncertain, although fortunately it mostly affected a sparsely populated area.  This makes dispensing aid, however, a difficult task to many of the remote villages and people.  Generally speaking, much of Africa is ill-prepared for such natural disasters. 
