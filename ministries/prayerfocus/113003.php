                          <p align="left"><strong>What 
                            is AIDS?</strong><br>
                            Discovered more than 20 years ago, AIDS (acquired 
                            immunodeficiency syndrome) is caused by the virus, 
                            HIV (human immunodeficiency virus). This virus, according 
                            to its name, attacks special immune cells in its host, 
                            destroying that person&#8217;s ability to fend off 
                            disease. When the immune cell count drops below a 
                            certain level, the person is said to have developed 
                            full-blown AIDS. These patients then become susceptible 
                            to &#8220;opportunistic infections&#8221; which would 
                            be harmless to a person with a normal immune system. 
                            It is these infections that eventually lead to death.</p>
                          <p align="left">World 
                            AIDS Day has been designated at December 1st. On this 
                            day, the World Health Organization and UNAids publish 
                            a report on the state of the AIDS epidemic for that 
                            year. Their findings as well as their predictions 
                            for the years to come are astounding.</p>
                          <div align="left"> 
                            <ul>
                              <li>40 
                                million people - are living with AIDS around the 
                                world. More than half of these are under the age 
                                of 24. Around 14,000 people are infected every 
                                day and there have been about 5 million new infections 
                                in 2003 to replace the 3 million who have died 
                                this year.<br>
                                <br>
                                </li>
                              <li> 
                                2/3 &#8211; of African countries have up to 40% 
                                of their population infected with AIDS. Many are 
                                calling this epidemic the &#8220;Genocide of a 
                                generation&#8221;. There have been towns which 
                                have lost their entire adult population and companies 
                                that have had to close because they lost all their 
                                workers to AIDS.<br>
                                <br>
                                </li>
                              <li> 
                                52 million &#8211; is the conservative estimate 
                                of new infections in China, Russia, India, Ethiopia, 
                                and Nigeria by the year 2010. India, China, and 
                                Russia alone make up half the world&#8217;s population 
                                and already show signs of the AIDS virus taking 
                                hold. 29 million of these infections can averted 
                                if the prevention techniques and care measures 
                                already successful today spread are implemented 
                                worldwide. &#8220;Today's report warns regions 
                                experiencing newer HIV epidemics that they can 
                                either act now or pay later - as Africa is now 
                                having to pay.&#8221; &#8211; Dr. Peter Piot, 
                                executive director of UNAIDS<br>
                                <br>
                                </li>
                              <li> 
                                10.5 billion &#8211; US dollars is the estimated 
                                cost of fighting the disease by 2005. This figure 
                                was only $300 million in 1999.</li>
