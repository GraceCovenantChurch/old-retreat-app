                          causes of the conflict are extremely complex, but they 
                          find their root in the long-standing ethnic rivalries 
                          and disputes over Congo&#8217;s rich natural resources. 
                          Nearly every country that touches Congo&#8217;s borders 
                          are involved directly in the dispute, leading experts 
                          to call this Africa&#8217;s world war. </p>
                        <p align="left">In 
                          2001, Joseph Kabila became president after his father&#8217;s 
                          assassination and surprised the government by immediately 
                          calling for a peace plan. The road has been extremely 
                          rocky. Most recently, leaders of the various rebel groups 
                          have been called together to form a traditional tribal 
                          government, with each serving as vice president under 
                          Kabila. However, the inaugural meeting failed to happen 
                          after two main rebel groups failed to show. </p>
