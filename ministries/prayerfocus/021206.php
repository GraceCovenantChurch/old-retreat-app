                      <p align="left"><strong>Japan: A Wayward Culture - February 12, 2006</strong></p>
<p>
Japan, a financial behemoth in East Asia, has dominated much of the global economy in the past few decades.  Its influence extends to the globalized culture permeating worldwide.  However, Japan itself suffers from much moral depravity and godlessness.  Recently, suicide pacts from the internet have emerged as a disturbing trend in Japanese culture.  Young people meet on the Web to arrange a time and place to commit suicide as a group.  The number of web suicide pacts has risen sharply from 34 in 2003 to 55 in 2004 and 91 in 2005.
<p>
Japan is 0.7% Christian.  Historically, Japanese culture has little deistic religion at all.  Shinto and Buddhist traditions dominate the spirituality of the Japanese people.  Reaching people in Japan with Christ’s love is incredibly difficult because of the cultural biases and moral bankruptcy.
<p>
<b>Please pray for:</b>
<ul>
<li><b>The depraved culture.</b>  Sexual deviance and internet suicide pacts coexist with refined and affluent cultural taste.  Pray that God would provide some type of moral compass for the people of Japan that would lead them not to seek sin or death, but to seek Himself.</li>
<li><b>The Christian church.</b>  Japan is widely considered one of the most difficult endeavors in Christian missions.  Please pray that barriers would be overcome and strongholds would be broken as the gospel is proclaimed, a gospel of the fullness of life in Christ that awaits the Japanese.  Pray that workers there would not be discouraged but would press on for His glory.</li>
</ul>
