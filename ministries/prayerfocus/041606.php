                      <p align="left"><strong>The Persecuted Church - April 16, 2006</strong></p>
<p>
<i>You will be betrayed even by parents, brothers, relatives and friends, and they will put some of you to death. All men will hate you because of me. But not a hair on your head will perish.  By standing firm you will gain life.</i><br>
- Luke 21:12-13, 16-19 (NIV)
<p>
Many Christians today are being tortured and martyred for their testimony of Christ as they follow His path in denying themselves and bearing their crosses (Matt 16:24-25).  During this season of remembering Christ’s suffering and resurrection, let us also pray for the suffering and eventual victory of our brothers and sisters in the persecuted church.
<p>
<b>Please pray for:</b>
<ul>
<li><b>Strength and peace to the persecuted saints.</b>  We serve the same God and therefore we can fellowship with the brothers and sisters around the world in prayer.  Pray that God would give them peace and joy in the midst of suffering and persecution for the sake of His gospel.</li>
<li><b>Spiritual fruit from their suffering.</b>  The Western church often does not emphasize a theology of suffering as a means of grace, but overseas in many places it is prominent.  Pray that in the suffering of the church, Christ would be proclaimed and fruit would be borne.</li>
<li><b>The support of the entire Church.</b>  Pray that many would rise up to join the persecuted, support them, and encourage them as we maintain a close hold on the reality of the persecutions, so that we could carry their burdens for them and pray for them who cannot pray.</li>
</ul>
