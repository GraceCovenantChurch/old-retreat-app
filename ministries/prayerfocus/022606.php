                      <p align="left"><strong>Lent: Suggestions for Application - February 26, 2006</strong></p>
<p>
Fasting and prayer generally marks the Lenten season.  It can be looked at as a time of austerity and sadness, but as our church participates in Lent, let us consider it a time of fasting from the world but feasting on the presence of Christ.  Repentance, self-examination, prayer, and fasting are not penances in a masochistic attempt at lowering our self-esteem, but rather they are means by which we can more fully experience God’s grace.
<p>
<b>As Lent approaches, we encourage you to make a firm and realistic commitment to:</b>
<ul>
<li><b>Fasting.</b>  Fasting can, but does not necessarily, entail abstaining from food. Some ways to consider fasting may include certain meals, like lunch on certain days, or certain types of food, like red meat or fast food.  Other ways to fast may include activities, like limiting the TV being watched or not doing schoolwork on Sundays.  Please choose a way to stretch yourself (but not kill yourself) so that you can hunger for more of God in your life.</li>
<li><b>Prayer and more personal time with God.</b>  As we free up more time and fast from the things of the world, please fill that time with prayer and intimate times with the Lord.  Consider a Bible reading plan, like reading through the gospels, or verse memorization.  Also contemplate the frequency and intensity of your prayer life.  One of the primary benefits of fasting is more powerful prayer.  Consider making a concrete commitment to pray during a certain fifteen minutes of the day or attending our weekly morning prayer meetings.</li>
</ul>
