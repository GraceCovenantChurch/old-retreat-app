                      <p align="left"><strong>Urbana - December 10, 2006</strong></p>
<p>
Urbana is a InterVarsity’s student missions conference that seeks to transform students and develop world changers.  Held triennially since 1946, Urbana has four chief objectives: declare God’s character and mission as revealed in Scripture, inform participants about the current realities in global missions, call all participants to join God’s mission, and motivate participants to serve in global and crosscultural missions.
<p>
Urbana 06 will take place December 27-31, 2006 in St. Louis, Missouri.
<p>
<b>Prayer Requests:</b>
<ul>
<li><b>Diversity of cultural input, expression, and involvement:</b> For more than half a century, Urbana has been pursuing ethnic and cultural diversity in its program and participants with a twofold purpose: to call all ethnic groups to God’s mission, and to give participants a small experience of what it means to be cross-cultural.  Pray that God continues to work across cultural boundaries both during and after the conference.</li>
<li><b>Logistics:</b> Since the 1990s, the conference has averaged approximately 20,000 participants.  The move to St. Louis was made, in part, to accommodate for even more people.  Pray for the participants’ safety in travels as well as a smooth flow of events.</li>
<li><b>Presence of the Spirit:</b> Most importantly, pray that God’s will be done during the conference and that the Bible studies, seminars, and worship sessions are all Word-centered and Spirit-guided.</li>
</ul>
