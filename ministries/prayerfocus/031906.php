                      <p align="left"><strong>Political Discord in Thailand - March 19, 2006</strong></p>
<p>
Last week, peaceful protests arrived in Bangkok.  Current president Thaksin Shinawatra is under fire for a recent sale of his family firm, an important national asset worth $1.9 billion, to Singaporean investors.  An upcoming election on April 2 appears riddled with problems because many of the protesters have asserted a boycott on the polls.  By national law, a candidate must win 20% of the electorate and that all 500 seats of parliament must be filled.  A boycott endangers the legitimacy of the vote and undermine the election, untested waters for the Thai government.
<p>
Thaksin support comes from the rural areas, where he is popular because of debt relief and low-cost healthcare that behooves many farmers.  Opposers claim political corruption and disloyalty.  While protests have remained nonviolent, the intensity on both sides continues to escalate.  Thailand is one of the most quickly developing countries in Southeast Asia and holds great influence on the entire region.  Political and economic struggles would create detrimental impact in many neighboring countries.
<p>
