                        <p align="left"><strong>Country 
                          Background</strong><br>
                          Liberia has been in a state of almost continuous civil 
                          war for the past few decades. The most recent conflict 
                          has been between two different rebel forces and the 
                          government troops led by controversial President Charles 
                          Taylor. Half of the rebels fight for Liberians United 
                          for Reconciliation and Democracy (LURD) and the other 
                          half fight for a group called Movement for Democracy 
                          in Liberia (MODEL). In the last few weeks, fighting 
                          has been extremely intense in the capital city of Monrovia. 
                          </p>
                        <p align="left"><strong>Recent 
                          Updates</strong><br>
                          Long awaited peacekeepers finally arrived in Monrovia 
                          this week. The Economic Community of Western African 
                          States (ECOWAS) has promised to send more enforcements 
                          of peacekeepers to support the units that came this 
                          week. As part of the peace deal moderated by the UN, 
                          Charles Taylor agreed to step down from office on Monday 
                          and leave his Vice President Moses Blah temporarily 
                          in control. </p>
                        <p align="left"><strong>Liberian 
                          Facts</strong><br>
                          Population: 3.3 Million (2003) (UN)<br>
                          Capital: Monrovia<br>
                          Official Languages: English, 29 African languages<br>
                          Major Religions: Atheist or traditional non-religious 
                          beliefs<br>
                          Life Expectancy: 41 years (men), 42 years (women) (UN)<br>
                          Average Annual Income: US $140 (World Bank, 2001)</p>
                        <p align="left"><strong>Prayer 
                          Requests</strong></p>
                        <div align="left"> 
                          <ul>
                            <li><strong>Smooth 
                              Transitions: </strong>If President Taylor follows 
                              through with his promises, he will be the first 
                              leader in decades to leave office peacefully. However, 
                              spokesmen for the government warn that his departure 
                              could mean even more bloodshed if the delicate balance 
                              already achieved does not withstand the pressures 
                              of his departure. Please pray for the transition 
                              of power and leaders involved. Pray that they would 
                              not seek selfish gain but instead hold true to the 
                              agreements they have made. <br>
                              <br>
                              </li>
                            <li><strong>The 
                              Peacekeepers</strong>: There are currently only 
                              two units of peacekeepers in Monrovia and enforcements 
                              are absolutely necessary for maintaining the fragile 
                              peace maintained. Please pray for the cooperation 
                              of the surrounding African countries. Also pray 
                              for the peacekeepers as they work in the areas of 
                              harshest fighting against rebels numbed by years 
                              of fighting. <br>
                              <br>
                              </li>
                            <li><strong> 
                              The People of Liberia</strong>: About 250,000 refugees 
                              are stranded in Monrovia without adequate food, 
                              shelter, medicine, or help. The peacekeepers have 
                              been hailed by these people as heroes, but the conflict 
                              is far from over and there is much work to be done. 
