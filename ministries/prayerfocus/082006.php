                      <p align="left"><strong>Ethiophia: Flooding - August 20, 2006</strong></p>
<p>
Ethiopia is Africa's oldest independent country and also one of its poorest states. Famine, war and ill-conceived policies brought millions to the brink of starvation in the 1970s and 1980s. This past week, a flash flood swept through southern Ethiopia killing at least 125 people, a week after more than 250 died in a similar disaster in the east of the country. The number of deaths has risen after the Omo river and tributaries burst their banks - with fears the toll countrywide could now top 800. In the far north, thousands of people in Tigray province are battling floods along the Tekezie river. The authorities in the town of Dire Dawa have declared the area a disaster zone, with the many still missing, and over 10,000 displaced from their homes.   Weather forecasters say heavier than usual rains are expected in the coming weeks across much of Ethiopia.
