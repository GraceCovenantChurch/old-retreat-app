                      <p align="left"><strong>Operation Christmas Child - November 04, 2006</strong></p>
<p>
For over 35 years, Samaritan's Purse has done their utmost to follow Christ's command by going to the aid of the world's poor, sick, and suffering. They are an effective means of reaching hurting people in countries around the world with food, medicine, and other assistance in the Name of Jesus Christ. This, in turn, earns us a hearing for the Gospel, the Good News of eternal life through Jesus Christ.
Operation Christmas Child brings joy and hope to children in desperate
situations around the world through gift-filled shoe boxes and the good news of God's love.  This program of Samaritan's purse provides an opportunity for people of all ages to be involved in a simple, hands-on missions project while focusing on the true meaning of Christmas – Jesus Christ.
<p>
<b>Prayer Requests:</b>
<ul>
<li>Most children who receive shoe box gifts live in desperate circumstances, suffering from abject poverty, sickness, war, or natural disaster.  Pray for the hurting children and their families whose lives will be touched this year through Operation Christmas Child.  Pray especially that many hearts will be opened to God's love and the Good News of Jesus Christ.</li>
<li>Pray for ministry partners in countries around the world.  Gift-filled shoe boxes are distributed in some countries that are not receptive to the Gospel of Jesus Christ.  Pray that doors will be opened and that these gifts will be effective tools for evangelism.</li>
<li>Pray for everyone involved in the process of delivering shoe box gifts as they deal with the complex, logistical challenges of fulfilling OCC's mission around the world in a timely manner.</li>
</ul>
