                      <p align="left"><strong>Yemen: An Unreached Land - December 04, 2005</strong></p>
<p>
Yemen, a country south of Saudi Arabia in the Middle East, has suffered through many of the conflicts in its area over the last four decades: three civil wars, conflicts with neighboring states, the effects of the Gulf War, Somalia’s collapse, and the Ethiopian-Eritrean war.  Yemen is a borderland for corruption, kidnappings, drug trafficking and massive arms trade.
<p>
Yemen is one of the least evangelized countries in the world, with only .05% of the population being Christians.  The nation is under shari’a law, the Muslim religious/legal system that governs all aspects of life.  It is completely illegal for Muslims to become Christians and for non-Muslims to proselytize.  
<p>
<b>Please pray for:</b>
<p>
<ul>
 <li><b>Stability across the national condition.</b>  Please pray that the nation’s leaders would fall under the influence of the Holy Spirit and begin to take measures to purge the nation from its corruption and brokenness.</li>
 <li><b>The triumph of the gospel through various channels.</b>  While Yemen seems to be one of the most impossible places to bring the gospel (on the persecution index it ranks 5th in the world), many opportunities are opening up for expatriates to get involved with business, education, health and development programs.  Please pray that people would bring the gospel into Yemen’s borders and that God would move with great power even in those limited encounters.</li>
</ul>
