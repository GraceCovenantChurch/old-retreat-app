<p>
Dr. Steve Lee, his wife Betty, and their five children Joy, Noelle, Luke, Bethany, and Judah have been serving the Lord in Kenya through Africa Inland Mission International since August 2004.  Their primary focus in Kenya includes long-term medical and church planting work in Kapsowar.
