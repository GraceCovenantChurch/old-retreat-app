<p>
<b>Prayer Requests:</b>
<ul>
<li><b>Relationships:</b> Pray for relationships with local congregations in setting the stage for further ministries, such as pastors’ training seminars.</li>
<li><b>Perseverance:</b> Pray that the family continues to follow the footsteps of Christ and love the people of Kenya as Christ loves and identifies with them.</li>
<li><b>God’s Favor:</b> Pray that God would move the hearts of the political leaders of Kenya so they would assist in the expansion of God’s kingdom.</li>
<li><b>Increased Financial Support:</b> Pray for increased funding for the Kapsowar Hospital so that it can expand its medical ministries, which include mobile clinics and HIV/AIDS programs are a focus.</li>
