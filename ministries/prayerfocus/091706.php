Acts Ministries International (AMI) began in 2002 with a group of pastors who had a vision to restore the early church that was seen in Acts 2.  The pastors envision AMI to be more than a denomination or an association.  It is a community of churches seeking to model the spirituality, vision, and partnership exemplified in the book of Acts.  AMI is committed to developing disciples of Christ within our churches, as well as spreading the gospel message to unreached people through church planting and missions work.
