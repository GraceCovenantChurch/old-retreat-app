                      <p align="left"><strong>The Democratic Republic of Congo - July 30, 2006</strong></p>
<p>
The Democratic Republic of Congo lies in the center of Africa, towards the eastern coast. It is a country that has been gripped by civil war since gaining independence from the Belgians in 1960. An estimated 4 million people have died during this crisis, while corrupt politicians and their militias were fighting over control of the nation's vast natural resources such as gold, copper, and coltan (a raw material used in high-tech goods such as cell phones). This war has been termed "Africa's First World War," as such, this area is host to the largest UN peace keeping mission to date.
Population: 56 million (UN, 2005)
<p>
Today, July 30, the Congolese people are to enjoy their first fully democratic vote.
<p>
<b>Prayer requests:</b>
<ul>
<li><b>Today's elections:</b> Thank God for today's elections and pray that the Congolese will vote for a government who will act in the nation's interests.</li>
<li><b>The AIDS epidemic and sex trafficking:</b> The DR of Congo is in the heart of the AIDS epidemic, where thousands  die due to lack of education and treatment. Sex trafficking exacerbates the crisis, as the militias are free to use thousands of women as slaves.</li>
<li><b>Economy:</b> The DR of Congo has the potential to be a well-off nation that cares for its citizens but this can only happen with correct handling of the economy and the nation's natural resources.</li>
<li><b>Christians in the DR of Congo:</b> Pray that God would strengthen them and give them hope. Pray that they would live out the Gospel in this area.</li>
</ul>
