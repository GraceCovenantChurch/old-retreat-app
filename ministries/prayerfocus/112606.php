<p>
Operation World is a prayer guide published by Worldwide Evangelization for Christ (WEC) International. It is one of the most important supplementary works to WEC’s overall purpose of planting churches among the least reached people in the world. It contains a profile on every country in the world, information about its unreached people groups, and relevant prayer requests. The book serves as a tool for Christians everywhere to pray for the needs of the missions work happening across the world.
Our sister Jenny Yuh departed for London in June 2006 to work with WEC on the next edition of Operation World.
<p>
<b>Prayer Requests:</b>
<ul>
<li><b>Effectiveness in ministry:</b> The team is relatively new, with only one member remaining from the last edition. The next edition is slated to be published in 2008, and there are a number of obstacles that could hinder the process. Pray that the updates and advances the team envisions would be realized and that everything from foreign contacts to computer stability would run smoothly.</li>
<li><b>Spiritual protection:</b> Although it is a different type of ministry than typical missions work, Operation World’s work is close and personal to the front lines of the missions field. Pray that God would show favor on this work for His ultimate glory as He establishes His kingdom.</li>
<li><b>Unity:</b> Pray that the team itself would find Spirit-led unity to accomplish the task, as working on a deadline can lead to stress and division.</li>
</ul>
