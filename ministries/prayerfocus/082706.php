                      <p align="left"><strong>Senegal: A Nation Torn - August 27, 2006</strong></p>
<p>
Senegal is on the western-most part of the bulge of Africa and has been plagued by a long-running, separatist war in the southern Casamance region claiming hundreds of lives. The conflict broke out over claims by the region's people that they were being marginalised by the Wolof, Senegal's main ethnic group.
The government and rebels signed a peace pact at the end of 2004, raising hopes for reconciliation. Yet separatists from a faction of the Movement of Democratic Forces of Casamance (MFDC), began a new campaign against government forces earlier this year. The Senegalese government launched an offensive against the MFDC rebels near the Gambian border last week. Many people have crossed into The Gambia after fighting between Senegal's army and rebels in the southern Casamance region.
There are some 12,500 Senegalese refugees in The Gambia now, where many are left homeless and without food.
