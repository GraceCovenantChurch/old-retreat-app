                        <p align="left">Founded 
                          by freed American slaves, Liberia is Africa&#8217;s 
                          oldest republic. Ironically, only 5% of the population 
                          are the descendants of freed slaves. The rest of the 
                          population consists of native Africans. This small group 
                          of descendents have developed into a socially and economically 
                          elite group of Americo-Liberians. The current Liberian 
                          president, Charles Taylor, is a part of this group.</p>
                        <p align="left">For 
                          decades now, Liberia has been in almost constant civil 
                          war, beginning with Samuel Doe who overthrew the ruling 
                          elites due to food shortages. In 1980, Charles Taylor 
                          took power in Liberia&#8217;s first successful coup 
                          d&#8217;etat. Taylor was then elected as president in 
                          the 1997 elections, which many critics labeled a charade. 
                          During Taylor&#8217;s rule, the rebel group named Liberians 
                          United for Reconciliation and Democracy (LURD) developed 
                          and has been gaining power in the north and west. Another 
                          group, named the Movement for Democracy in Liberia (Model) 
                          has just recently developed making the civil war into 
                          a three-way battle for power.</p>
                        <p align="left"><strong><br>
