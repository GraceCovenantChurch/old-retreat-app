                      <p align="left"><strong>Summer Innercity Missions - April 29, 2007</strong></p>
<p>
This summer, GCC is sending a young adult missions team to the Over the Rhine neighborhood of Cincinnati, Ohio.  Over the Rhine has a vibrant African-American community, but it also faces poverty, violent crime and open-air drug trade.  The team will be partnering with Pastor Paul and Pastor Johann who have been working in this area as lay evangelists for over 10 years. The team will be going to bless their efforts and learn more about how to share Christ's love in the inner city. The team will also share the love of Christ with those in OTR through Vacation Bible School, open air ministry, street evangelism, and service projects to help local residents.  The team will be in Cincinnati from July 19 to July 28.
<p>
<b>Prayer Requests:</b>
<ul>
<li><b>Hearts for the People:</b> Pray that God would fill our hearts with love for the people of the Over the Rhine community.</li>
<li><b>People of OTR:</b> Pray that God would already be at work in the hearts of the people.  Pray that his love and mercy would be covering them in whatever circumstances they face.</li>
<li><b>Leadership:</b> Please pray for wisdom in the leadership of the team during training and the trip.  Also pray for Pastor Paul and Pastor Johann as they minister to the community and prepare for the team to arrive.</li>
<li><b>Team Unity:</b> Pray that the team members would unify as one to support one another as they prepare their hearts, minds, and bodies to serve in the name of Jesus Christ.</li>
</ul>
