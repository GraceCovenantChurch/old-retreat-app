                      <p align="left"><strong>The Season of Lent: A Time to Feast - February 19, 2006</strong></p>
<p>
Lent has been observed by Christian churches as the period from Ash Wednesday through to Palm Sunday, in part remembering the forty days that Jesus spent in the wilderness after his baptism by John the Baptist, preparing for his ministry on earth.  As a church tradition, it is also a time spent in anticipation of Easter.  For some, that meant preparing for receiving a baptism at Easter; for others, it simply meant meditating and drawing nearer to Christ as the church prepared to celebrate the Savior’s death and resurrection.
<p>
Fasting and prayer generally marks the Lenten season.  It can be looked at as a time of austerity and sadness, but as our church participates in Lent, let us consider it a time of fasting from the world but feasting on the presence of Christ.  Repentance, self-examination, prayer, and fasting are not penances in a masochistic attempt at lowering our self-esteem, but rather they are means by which we can more fully experience God’s grace.
<p>
<b>As Lent approaches, please consider:</b>
<ul>
<li><b>Fasting.</b>  Fasting does not necessarily entail abstaining from food. We will provide more information on ways to fast, but please consider making a commitment to surrender your rights to something in dedication to Christ.</li>
<li><b>Prayer.</b>  Lent is not a season of self-inflicted suffering but rather a time to draw nearer to God, especially in prayer.  Please think about practical ways you can incorporate more prayer in your life during Lent.</li>
</ul>
