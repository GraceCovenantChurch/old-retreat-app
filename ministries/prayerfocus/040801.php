                        <p align="left">PASSION 
                          WEEK The resurrection of our Lord on Easter Sunday some 
                          two thousand years ago is the most significant event 
                          in all of history. His victory over death assures us 
                          of His promise, that those who trust in Him will have 
                          eternal life. It is no wonder that Easter is such a 
                          joyous time for Christians. However, this victory came 
                          at a great cost – the crucifixion and death of our Lord 
                          Jesus. Without a clear understanding of what the Lord 
                          went through on our behalf, we cannot fully grasp the 
                          immensity of the grace that He has given us. </p>
                        <p align="left">Passion 
                          Week is the week leading up to the crucifixion of Christ 
                          when we can reflect upon this ultimate sacrifice that 
                          was made for us. Passion Week begins on the Friday before 
                          Palm Sunday (today) when Christ arrived in Bethany and 
                          continues through up until his resurrection on Easter. 
                          Below is a brief description of what Christ did from 
                          Palm Sunday until his crucifixion and resurrection.</p>
                        <p align="left"><b>DESCRIPTION 
                          OF PASSION WEEK </b></p>
                        <div align="left"> 
                          <ul>
                            <li> <b>Palm 
                              Sunday</b> – Jesus rode into Jerusalem on a donkey, 
                              fulfilling an ancient prophecy. The crowd welcomed 
                              him with “Hosanna” and the words of a Psalm ascribing 
                              to him a Messianic title as the coming King of Israel. 
                              <br>
                              <br>
                              </li>
                            <li> <b>Monday</b> 
                              – Jesus returns to the temple to see people trading 
                              and exchanging money. Jesus then drove them out 
                              of the temple and overturned their tables. <br>
                              <br>
                              </li>
                            <li>  
                              <b>Tuesday & Wednesday</b> – On Tuesday, Jesus taught 
                              in parables and warned the people against the Pharisees. 
                              There is no mention in the Gospels of what Jesus 
                              did on Wednesday (it is believed that he may have 
                              rested). <br>
                              <br>
                              </li>
                            <li>  
                              <b>Thursday</b> – The Last Supper. The loaf of bread 
                              and cup of wine that was eaten during the Last Supper 
                              represented his body soon to be sacrificed and his 
                              blood soon to be shed. Jesus then went to the Garden 
                              of Gethsemane and prayed. <br>
                              <br>
                              </li>
                            <li>  
                              <b>Friday</b> – The Crucifixion. Jesus was crucified 
                              alongside two criminals. <br>
                              <br>
                              </li>
                            <li> 
                              <b>Easter Sunday</b> – Jesus was resurrected!<br>
                              </li>
                          </ul>
                        </div>
