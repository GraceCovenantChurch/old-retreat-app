<p>
<span style="font-weight: bold;">Lent</span><br /><br />Lent has been observed by Christian churches as the period from Ash Wednesday through Palm Sunday, remembering the forty days that Jesus spent in the wilderness after his baptism by John the Baptist as He prepared for His ministry on earth.  As a church tradition, it is also a time spent in anticipation of Easter.
Fasting and prayer generally mark the Lenten season.  As our church participates in Lent, let us consider it a time of fasting from the world but feasting on the presence of Christ.  Repentance, self-examination, prayer, and fasting are some of the means by which we can more fully experience God’s grace.  This year, lent started on February 21 and will end on April 1.
<p><b>Prayer Requests:</b></p>
<ul>
<li><span style="font-weight: bold;">Fasting: </span>Fasting does not necessarily entail abstaining from food.  Pray about possibly making a commitment to surrender your rights to something in dedication to Christ.</li>
<li><span style="font-weight: bold;">Prayer: </span>Lent is not a season of self-inflicted suffering, but a time to draw nearer to God, especially in prayer.  Pray for God to show you how to incorporate more prayer in your life during Lent, and for that prayer time to be intimate and powerful.</li></ul>
