<p>
<br />Since October 2001, the United States and aligning powers have launched a series of military strikes in Afghanistan in reaction to the attack of 9/11.  These initiatives are targeted at (1) destroying al-Qaeda, an international alliance of militant jihadist groups, (2) capturing self-proclaimed instigator of the September 11th attacks, Osama bin Laden, and (3) dismantling the Taliban, a militant jihadist movement and an oppressive regime.  The operation has proven largely unsuccessful.  The region grows increasingly unstable and dangerous as warlords and Taliban guerrillas are terrorizing the people.  With security compromised, opium production flourishes and high-authority positions are now filled by top drug traffickers.  Thousands of civilians have also died as a result of the conflict.
<p>
Prayer Requests
The people of Afghanistan:  Human rights violations mark the history of Afghanistan in recent decades, from the invasion of the Soviet Union to persecution by the Taliban.  Many civilians have been killed, and others are without electricity, food, or medicine.  Please pray for these people as they endure such hardship.
Christians in Afghanistan: Most of the 3,000-5,000 Christians in Afghanistan are former Muslims who converted, and others come from abroad with foreign aid; evangelizing is not tolerated.  Christians both native and foreign continue to face persecution in Afghanistan, where it is said that Christians have no hope of survival.  Please pray for all Christians in Afghanistan, particularly those 23 kidnapped South Korean missionaries, as well as their families back home.
