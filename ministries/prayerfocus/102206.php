                      <p align="left"><strong>North Korea - October 22, 2006</strong></p>
<p>
The Democratic People’s Republic of North Korea detonated a nuclear bomb on October 9, 2006, prompting the United Nations Security Council to unanimously approve sanctions on the embattled country.  While the relatively small explosive force of the blast has led some to question the success of the test, it is clear that the DPRK has further strained its relations with the rest of the international community.
Meanwhile, the people of the country continue to suffer from massive food shortages.  The World Food Program estimates that it will need $100 million to meet its goal for North Korea this year, of which only 10 percent has been met.  The recent nuclear tests, along with the missile tests in July, have reduced donors’ desire to help.
<br /><br /><b>Prayer Requests:</b>
<ul>
<li><b>Humanitarian aid:</b> Pray for continued aid even amid the recent tests, and perhaps more importantly, for those donations to reach the people they are intended for.  North Korea does not allow for outside monitoring of the donations once it gets to the country.</li>
<li><b>Proper response from international community:</b> The international community finds itself in a conundrum, as it attempts to condemn the regime without increasing or prolonging the suffering of the North Korean people.  Pray for wisdom and for the punitive sanctions to bring about positive changes.</li>
<li><b>Openings:</b> The DPRK has long been one of the most closed off countries in the world, making it impossible for missionaries, humanitarian aid workers, or diplomats to have any real impact on the nation.  Pray that God would slowly open up the country so change can take place.</li></ul>
