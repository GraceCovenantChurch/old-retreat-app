                      <p align="left"><strong>Passion Week - April 01, 2007</strong></p>
<p>
<span style="font-weight: bold;">Passion Week</span><br /><br />Both inside and outside of the church, Easter seems to bring a renewed interest in spiritual matters.  Jesus’ resurrection from the dead is the cornerstone of our faith, and we know that because Jesus is alive, one day we will live forever.
<p>
Passion Week, the week immediately preceding Easter Sunday, marks Jesus’ last week here on Earth.  As a body, we would like to make this year’s Easter season a time where we can reflect on the Lord’s Passion, thank Him for His sacrifice, celebrate in His resurrection, and live in His victory.
</p>
<p><b>Prayer Requests:</b></p>
<ul>
<li><span style="font-weight: bold;">Services: </span>Pray for the Maundy Thursday, Good Friday, and Easter Sunday services.  Pray that each service would remind the congregation of His sacrifice and His victory.</li>
<li><span style="font-weight: bold;">Fasting: </span>Many members of the church abstain from eating certain foods during Easter season.  Pray that fasting would remind them of what really sustains life and that they would lean more heavily on God during this time.</li>
<li><b>Preparation: </b>Pray that people spend this week preparing their hearts for Easter, and that the power and hope that is found in the cross is not lost amid busy schedules or routine.</li></ul>
