                      <p align="left"><strong>Flooding in Thailand - January 15, 2006</strong></p>
<p>
Since late November, Thailand has been pounded by heavy rains.  Dozens of people have died from flooding and landslides, some remain reported missing, and overall more than 700,000 people have been affected in some way.  2,200 have been evacuated to 21 emergency shelters, public transportation has frozen, and schools have been closed.  Flood-related diseases, like conjunctivitis, are affecting more than 40,000.  
<p>
Thailand is one of the shining stars of the southeast Asia, with high commercial and technological development, as well as stable economy.  However, the nation is less than 1% Christian, the sex trafficking industry remains a massive problem, and separatist violence in the predominantly Muslim southern provinces continue to trouble the government.  
<p>
Please pray for:
<ul>
 <li><b>Recovery from the flooding.</b>  The needs include fresh water, supplies, medicine, and reconstruction resources.  Pray that Christians can provide relief and testify to the love of Christ in the midst of disaster.</li>
 <li><b>The local Thai church.</b>  Pray that the minimal Christian population would have the freedom and courage to boldly proclaim the gospel to the nation.  Pray for missionaries, like Pastor Milo Cho, and also for a large Burmese refugee population in Thailand of whom most have been persecuted for their faith.  Pray that God’s light would shine brighter in a land of great spiritual need.</li>
