    <td><h4>Testimonies of Christ - Sarah Lee</h4>
          <p align="left"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">I&#8217;m 
              here to share with you how God has worked in my life to bring me 
              where I am today. I titled my testimony &#8220;what does it mean 
              to be a Christian&#8221; because as I was preparing for it I felt 
              like that was the theme God used in my spiritual development.</font></p>
            <p align="left"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">So 
              what does it mean to be a Christian? As a child I thought being 
              a Christian meant going to church faithfully and having a Christian 
              family because I was born and raised in a Christian home so I learned 
              about Jesus at an early age and I did go to church and Sunday school 
              very faithfully. Even when I lived in Qatar in the Middle East I 
              went to an (sort of) underground church. So I found security as 
              a Christian in the fact that I went to church and had a Christian 
              family.</font></p>
            <p align="left"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><em><strong>First 
              step</strong></em><br>
              Then when I came to the States in Jr. high I was in a youth group 
              meeting one night and my youth group leader casually said that just 
              because you go to church, does not mean you are a Christian. She 
              said you had to ask Jesus to come into your life to be your personal 
              Lord and Savior and have a relationship with Him. </font></p>
            <p align="left"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">I 
              remember that comment really gripping my heart and making me stop 
              and think because it was the first time I ever wondered if I really 
              was a Christian or not because I didn&#8217;t remember ever praying 
              that before. So I got scared and when I went home that night I knelt 
              on my bed and prayed that Jesus would enter my heart and life. I 
              even made my younger sister do it because she was too young to be 
              in youth group and I wanted to make sure she was a Christian too.</font></p>
            <p align="left"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">So 
              that was my first step in understanding what it meant to be a Christian 
              ~ it wasn&#8217;t just about going to church or having a Christian 
              family but it was about having a personal relationship with Jesus 
              and having Jesus living in you. </font></p>
            <p align="left"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">So 
              after that I still led an outwardly evident Christian life all through 
              high school and it wasn&#8217;t until college that I took another 
              big step in understanding what it meant to be a Christian. This 
              was a very hard step for me because it involved dying to myself 
              so that Christ could live.</font></p>
            <p align="left"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><em><strong>Next 
              step</strong></em> <br>
              There were 2 areas of my life that I had to die to because I was 
              so stubbornly holding onto them and it was preventing my spiritual 
              growth. One area was my shyness and the other was my ambition and 
              I&#8217;ll explain why these areas were hindering me from experiencing 
              God more.</font></p>
            <p align="left"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><em><strong>Shyness 
              </strong></em><br>
              First, my shyness: as a child I was abnormally shy. In kindergarten 
              I had an accident because I was too shy to ask my teacher to go 
              to the bathroom. My teachers told my parents that in all their years 
              of teaching, they had never had a child so quiet. Kids used to tease 
              me and ask if I could talk or if I was mute. </font></p>
            <p align="left"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">I 
              was one of those students who never raised their hand to ask a question 
              in class. And when I got to college, this really affected my church 
              involvement because I dreaded being in a small group setting where 
              I would have to talk, share, and pray in front of people so I refused 
              to go to small group Bible study. </font></p>
            <p align="left"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><em><strong>Ambition 
              </strong></em><br>
              The second area was my ambition, which was to become a doctor. That 
              was my goal in life and the only thing I wanted to do. Everything 
              I did was geared towards achieving this goal so in college God took 
              second place over my ambition. </font></p>
            <p align="left"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Becoming 
              a doctor meant more to me that knowing God. I didn&#8217;t want 
              to spend the extra time going to Bible study when I could be studying 
              to get into med school. So because of this and because of my shyness, 
              I just went to church on Sundays. That was the only time I wanted 
              to commit to God.<br>
              <br>
              <em> <strong>Change</strong></em><br>
              Then it was in my last year of college that things started to change. 
              God used circumstances to start breaking me in these 2 areas of 
              my life. What happened was (this is kind of embarrassing) I had 
              applied to about 20 medical schools thinking I would surely get 
              into one of them but I got rejected by every single one of them. 
              I was very upset and depressed, my pride was hurt and I didn&#8217;t 
              know what I was going to do after college. <br>
              <br>
              So I decided to turn to God and the Bible for some help and some 
              answers because I didn&#8217;t know what else to do. So I studied 
              the Bible regularly on my own and the verse that transformed my 
              understanding of what a Christian should be was Galatians 2:20. 
              </font></p>
            <p align="left"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">When 
              I realized that my life was not my own because God bought it at 
              a costly price and that I should no longer live but let Christ live 
              through me, it was like a profound revelation for me. I felt like 
              my eyes had been opened and I could see clearly that being a Christian 
              meant dying to my selfish, self-centered desires and plans and letting 
              Christ take control even if it meant not becoming a doctor. </font></p>
            <p align="left"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">At 
              the same time this was happening, I heard a Sunday message about 
              being bold and for some reason that really challenged me to maybe 
              try going to small group Bible study and breaking out of my shell. 
              So I got the courage to go to small group and at first it was very 
              uncomfortable. </font></p>
            <p align="left"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">I 
              joined a women&#8217;s group for college seniors and I was so scared 
              that they would ask me to share and pray in front of them but with 
              every meeting, I slowly felt that it wouldn&#8217;t be so bad after 
              all. And studying God&#8217;s word with fellow Christians was exciting 
              and kept me coming back each week. </font></p>
            <p align="left"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">I 
              was starting to really enjoy small group but it was time to graduate 
              and leave Boston so I remember regretting that I had not joined 
              a small group sooner but little did I know that God had so much 
              more in store for me because after graduation, God brought me to 
              GCC.</font></p>
            <p align="left"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><em><strong>Conclusion</strong></em><br>
              So when I was at the point in my spiritual life where my heart was 
              opening and God was starting to break me I came to GCC and experienced 
              such exponential growth in areas like: understanding who God is, 
              spiritual disciplines, missions, Christ-centered relationships, 
              serving, Biblical examples of Christian living and much more. </font></p>
            <p align="left"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">I 
              was like a sponge soaking in everything. I felt like when I took 
              that step of faith and died to those 2 areas of my life, God opened 
              a floodgate of blessings.</font></p>
            <p align="left"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">So 
              that&#8217;s where I am today, in my 6th year at GCC, still soaking 
              in God&#8217;s blessings and being challenged to live out each day 
              this relationship I have with Jesus. So to conclude, what does it 
              mean to be a Christian? For me, it was a journey of discovering 
              that because Jesus died for me and purchased my life, I could not 
              live for myself any longer. </font></p>
            <p align="left"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">&#8220;I 
              have been crucified with Christ and I no longer live but Christ 
              lives in me. The life I live in the body, I live by faith in the 
              Son of God, who loved me and gave himself for me.&#8221; ~Galatians 
              2:20</font></p>
